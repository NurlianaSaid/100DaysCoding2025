package liana;

public class Day4 {
    public static void main(String[] args) {
        // kode (\t) menambah spasi
        System.out.println("nama :\tNurliana");
        
        // kode (\\) menambah garis miring terbalik pada output
        System.out.println("contoh : \\");
        
        // kode (\n) memindahkan teks ke garis baru
        System.out.println("Sedang \n belajar");
        
        // kode (\r) mengembalikan kursor ke awal baris
        System.out.println("Harus \r Semangat");
        
        // kode (\b) menghapus karakter sebelumnya
        System.out.println("Halo\b Dunia");
        
        // kode (\f) form feed, memindahkan ke halaman baru (tidak semua terminal mendukung)
        System.out.println("Halaman 1\fHalaman 2");
        
        // kode (\") menambah tanda petik ganda dalam string
        System.out.println("Dia berkata, \"Halo Dunia\"");
        
        // kode (\') menambah tanda petik tunggal dalam string
        System.out.println("Dia berkata, \'Halo Dunia\'");
    }
}
