public class Day9 {
    
    public static void main(String[] args) {
        //deklarasi variabel
        String nama;
        int nim;
        char ipk;
        
        //inisialisasi variabel setelah deklarasi(pengisian nilai)
        nama = "Nurlian";
        nim = 22551;
        ipk = '3';
        
        //update nilai variabel
        nama = "Nurliana";
        nim = 225514;
        ipk = '4';
        
        
        System.out.println("Nama Saya \t= \t"+nama);
        System.out.println("Nim Saya \t= \t"+nim);
        System.out.println("IPK Saya \t= \t"+ipk);
    }
    
}
